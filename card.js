class card {
    constructor(num, suit) {
        this.num = num;
        this.suit = suit;
    }
    getNum() {
        return this.num;
    }
    setNum(newNum) {
        this.num = newNum;
    }
    getSuit() {
        return this.suit;
    }
    setSuit(newSuit) {
        this.suit = newSuit;
    }
}